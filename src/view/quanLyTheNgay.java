    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String maThe = txtTaoTheNgay.getText();
        if (txtTaoTheNgay.getText().equals("")) {
            JOptionPane.showMessageDialog(jComboBox1, "Hãy nhập mã thẻ muốn thêm");
        } else {
            if (theController.themTheNgay(maThe)) {
                JOptionPane.showMessageDialog(jSeparator1, "Thêm thẻ ngày thành công");
                txtTaoTheNgay.setText("");
            } else {
                JOptionPane.showMessageDialog(jSeparator1, "Thẻ đã tồn tại trong hệ thống");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String maThe = txtXoaTheNgay.getText();
        if (maThe.equals("")) {
            JOptionPane.showMessageDialog(jSeparator1, "Hãy nhập mã thẻ cần xóa");
        } else {
            the t = theController.checkCoXe(maThe);
            if (t != null) {
                int i = JOptionPane.showConfirmDialog(jComboBox1, "Thẻ ngày đang được sử dụng để gửi xe (Biển số: " + t.getBienSoXe() + "), xác nhận xóa thẻ ?");
                if (i == 0) {
                    theController.xoaTheNgay(txtXoaTheNgay.getText());
                    JOptionPane.showMessageDialog(jComboBox1, "Xóa thẻ ngày thành công");
                }
            } else {
                theController.xoaTheNgay(txtXoaTheNgay.getText());
                JOptionPane.showMessageDialog(jComboBox1, "Xóa thẻ thành công");
            }
            txtXoaTheNgay.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

