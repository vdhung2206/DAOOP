    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
        String date1 = sdf.format(tuNgay.getDate());
        String date2 = sdf.format(denNgay.getDate());
        list = baoCaoThongKeController.thongKeDoanhThuTheThang(date1, date2, cbbLoaiXe.getSelectedItem().toString(),cbbLoai.getSelectedIndex());
        model.setRowCount(0);
        showTable();
        txtSoLuot.setText(String.valueOf(list.size()));
        int doanhThu=0;
        for(int i=0; i<list.size(); i++){
            doanhThu+= list.get(i).getTien();
        }
        txtDoanhThu.setText(String.valueOf(doanhThu));
    }//GEN-LAST:event_jButton2ActionPerformed


