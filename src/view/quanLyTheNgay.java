    public quanLyTheNgay() {
        initComponents();
        list = theController.layDanhSachTheNgay();
        model = (DefaultTableModel) jTable1.getModel();
        model.setColumnIdentifiers(new Object[]{"Mã thẻ"});
        txtTong.setText(String.valueOf(list.size()));
        txtConLai.setText(String.valueOf(theController.demTheNgayConLai()));
        txtDaDung.setText(String.valueOf(list.size() - theController.demTheNgayConLai()));
        showTable();
    }

    public void showTable() {
        for (the t : list) {
            model.addRow(new Object[]{t.getMaThe()});
        }
    }

    public void reset() {
        list = theController.layDanhSachTheNgay();
        model.setRowCount(0);
        txtTong.setText(String.valueOf(list.size()));
        txtConLai.setText(String.valueOf(theController.demTheNgayConLai()));
        txtDaDung.setText(String.valueOf(list.size() - theController.demTheNgayConLai()));
        showTable();
    }

    public void showDetail() {
        int selected = jTable1.getSelectedRow();
        txtXoaTheNgay.setText((String) model.getValueAt(selected, 0));
    }

