    public void showTable() {
        for (thongKeTheThang tt : list) {
            model.addRow(new Object[]{tt.getMaThe(), tt.getLoaiXe(), tt.getNgayDangKy(), tt.getNgayDongTien(), tt.getTien()});
        }
    }

    public void showDetail() {
        int selected = jTable1.getSelectedRow();
        jTextField1.setText((String) model.getValueAt(selected, 0));
        jTextField2.setText((String) model.getValueAt(selected, 2));
        jTextField3.setText((String) model.getValueAt(selected, 3));
        jTextField4.setText(String.valueOf(model.getValueAt(selected, 4)));
        jTextField5.setText((String) model.getValueAt(selected, 1));
    }

