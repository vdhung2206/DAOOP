    private void txtMaTheKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMaTheKeyTyped
        // TODO add your handling code here:
        if (cbbLoaiThe.getSelectedIndex() == 1) {
            if (evt.getKeyChar() == KeyEvent.VK_BACK_SPACE) {
                sb.deleteCharAt(sb.length() - 1);
            } else {
                sb.append(evt.getKeyChar());
            }
            the t = theController.layThongTinTheThang(sb.toString());
            if (t != null) {
                txtBienSoXe.setText(t.getBienSoXe());
                if (t.getMaLoaiThe().equals("4")) {
                    cbbLoaiXe.setSelectedIndex(2);
                }
                if (t.getMaLoaiThe().equals("5")) {
                    cbbLoaiXe.setSelectedIndex(1);
                }
                if (t.getMaLoaiThe().equals("6")) {
                    cbbLoaiXe.setSelectedIndex(0);
                }
            } else {
                txtBienSoXe.setText("");
            }
        }
        if (cbbLoaiThe.getSelectedIndex() == 2) {
            if (evt.getKeyChar() == KeyEvent.VK_BACK_SPACE) {
                sb.deleteCharAt(sb.length() - 1);
            } else {
                sb.append(evt.getKeyChar());
            }
            the t = theController.layThongTinTheVip(sb.toString());
            if (t != null) {
                txtBienSoXe.setText(t.getBienSoXe());
                txtViTri.setText(t.getMaViTri());
                if (t.getMaLoaiThe().equals("8")) {
                    cbbLoaiXe.setSelectedIndex(0);
                }
                if (t.getMaLoaiThe().equals("7")) {
                    cbbLoaiXe.setSelectedIndex(1);
                }
            } else {
                txtBienSoXe.setText("");
            }
        }
    }//GEN-LAST:event_txtMaTheKeyTyped

