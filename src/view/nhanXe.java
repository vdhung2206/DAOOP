class clockThread extends Thread {

    private JTextField txt;

    public clockThread(JTextField txt) {
        this.txt = txt;
    }

    public void run() {
        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss ");
        while (true) {
            Date now = new Date();
            String st = sdf.format(now);
            txt.setText(st);
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {

            }
        }
    }
}

