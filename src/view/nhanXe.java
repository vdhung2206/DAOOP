    private void cbbLoaiTheItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbLoaiTheItemStateChanged
        // TODO add your handling code here:
        if (cbbLoaiThe.getSelectedIndex() == 2) {
            txtViTri.setEnabled(true);
            cbbLoaiXe.removeItem("Xe dap");
            ArrayList<khu> list = khuController.layDanhSachKhuVip((String) cbbLoaiXe.getSelectedItem());
            cbbKhuVuc.removeAllItems();
            for (int i = 0; i < list.size(); i++) {
                cbbKhuVuc.insertItemAt(list.get(i).getMaKhu(), i);
            }
            cbbKhuVuc.setSelectedItem(list.get(0).getMaKhu());
        } else {
            if (cbbLoaiXe.getItemCount() <= 2) {
                cbbLoaiXe.addItem("Xe dap");
            }
            ArrayList<khu> list = khuController.layDanhSachKhuThuong((String) cbbLoaiXe.getSelectedItem());
            cbbKhuVuc.removeAllItems();
            for (int i = 0; i < list.size(); i++) {
                cbbKhuVuc.insertItemAt(list.get(i).getMaKhu(), i);
            }
            cbbKhuVuc.setSelectedItem(list.get(0).getMaKhu());
            txtViTri.setEnabled(false);
        }
    }//GEN-LAST:event_cbbLoaiTheItemStateChanged

